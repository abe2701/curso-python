'''
Tipos de dados
str - string - textos
int - inteiro - 12345
float - real/ponto fluente - 1.5, 10.6
bool - booleano/logico - True or False
'''

print('Abelardo', type('Abelardo'))
print(10, type(10))
print(10.1, type(10.1))
print(False, type(False))

# converter valores
num = '10'
print('10', type('10'), int(num))
convert = int(num)
